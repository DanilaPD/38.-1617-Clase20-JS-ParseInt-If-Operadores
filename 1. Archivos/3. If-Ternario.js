// El "ternario" es otro tipo de "if": No tiene "else if", solo hay dos estados:

const ladra = "guauguau";

// Condición === qué la hace "true" ? qué hace si es "true" : qué hace si es "false":
//! Condición ? true : false
console.log(ladra === "guauguau" ? "Es un perro." : "No es un perro.");

// El ternario no permite no tener qué hacer en caso de "false".

const cucharas = 15;

console.log(cucharas > 15 ? "Nos alcanzan." : "No nos alcanzan.");
